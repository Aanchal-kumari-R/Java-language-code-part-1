public class for_each_loop {   

    public static void main(string[] args) {
           
        int arr[] = {1,2,3,4,5};    

        for(int b:arr){ 
            System.out.println(b);
        }   

       for(int i = 0;i<arr.length;i++){ 
        System.out.print(arr[i]+" ");
       }
    }
    
}
