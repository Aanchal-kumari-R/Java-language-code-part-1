import java.util.Scanner;
 public class print_n_natural_number {
  
    public static void main(string[] args) {  
        int n;  
        System.out.println("Enter the number:");  
        Scanner sc = new Scanner(System.in);  
        n = sc.nextInt();  
        for(int i = 0; i<=n; i++){ 
            System.out.println(i);
        }
        
    }
}
