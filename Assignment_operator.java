public class Assignment_operator {
       

    public static void main(string[] args) {
        int a = 6;  
        System.out.println(a);    

        a+=10;  // a= a+10 =16
        System.out.println(a);    
        
        a-=10;  // a = a-10 = 16-10 = 6;
        System.out.println(a);  

        a*=10;   // a = a*10 = 6 *10 = 60
        System.out.println(a);   

        a/=10;  // a = a/10 = 60/10 = 6
        System.out.println(a);   

        a%=10; // a = a%10 = 6%10 = 6;  here we use integer so a = 6  
        System.out.println(a);     

        
    }
}
